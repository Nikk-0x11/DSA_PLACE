#include <iostream>
using namespace std;

int main()
{
	int a = 17;
	char b = 'n';
	double c = 14.3;
	long int d = 345;
	cout << "integer: " << a << endl;
	cout << "character: " << b << endl;
	cout << "ascii of character: " << int(b) << endl;
	cout << "double: " << c << endl;
	cout << "long int: " << d << endl;

	return 0;
}